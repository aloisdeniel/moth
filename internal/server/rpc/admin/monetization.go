func (h *MonetizationHandler) billingCredentials(ctx context.Context, projectID string) (store.BillingCredentials, error) {
	creds, err := h.store.GetBillingCredentials(ctx, projectID)
	if errors.Is(err, store.ErrNotFound) {
		return store.BillingCredentials{}, nil
	}
	if err != nil {
		return store.BillingCredentials{}, connect.NewError(connect.CodeInternal, err)
	}
	return creds, nil
}

func productsProto(products []store.Product) []*adminv1.Product {
	out := make([]*adminv1.Product, 0, len(products))
	for _, p := range products {
		out = append(out, productProto(p))
	}
	return out
}

func syncStatusProto(s string) adminv1.ProductSyncStatus {
	switch s {
	case store.ProductSyncInSync:
		return adminv1.ProductSyncStatus_PRODUCT_SYNC_STATUS_IN_SYNC
	case store.ProductSyncDrift:
		return adminv1.ProductSyncStatus_PRODUCT_SYNC_STATUS_DRIFT
	case store.ProductSyncError:
		return adminv1.ProductSyncStatus_PRODUCT_SYNC_STATUS_ERROR
	default:
		return adminv1.ProductSyncStatus_PRODUCT_SYNC_STATUS_PENDING
	}
}

func manualStepProto(m setup.ManualStep) *adminv1.GuidedStep {
	g := &adminv1.GuidedStep{Title: m.Title, Detail: m.Reason, Url: m.URL}
	for i, ins := range m.Instructions {
		g.Values = append(g.Values, &adminv1.GuidedValue{Label: fmt.Sprintf("step %d", i+1), Value: ins})
	}
	return g
}

func formatMicros(micros int64) string {
	return fmt.Sprintf("%.2f", float64(micros)/1_000_000)
}

// stripeWebhookGuidedStep is the honest "no webhook secret stored" step: the
// signing secret is revealed by Stripe exactly once at endpoint creation, so
// the admin cannot wire it after the fact — `moth setup billing` creates the
// endpoint and persists the secret in the same run, or the operator registers
// it in the Stripe dashboard and pastes the secret into store credentials.
func stripeWebhookGuidedStep(slug, baseURL string) *adminv1.GuidedStep {
	endpoint := strings.TrimSuffix(baseURL, "/") + "/billing/stripe/webhook/" + slug
	return &adminv1.GuidedStep{
		Title:  "Wire the Stripe webhook",
		Detail: "moth holds no Stripe webhook signing secret, so subscription events cannot be verified. Run `moth setup billing` to create the endpoint via the API and store the secret, or register it in the Stripe dashboard and paste the signing secret into store credentials.",
		Url:    "https://dashboard.stripe.com/webhooks",
		Values: []*adminv1.GuidedValue{
			{Label: "command", Value: "moth setup billing --project " + slug + " --stripe-secret-key <key>"},
			{Label: "endpoint URL", Value: endpoint},
			{Label: "events", Value: strings.Join(setup.StripeWebhookEvents, ", ")},
		},
	}
}

// guidedSteps returns the store-level manual steps the catalog APIs cannot
// perform, with the exact values to paste (honest automation — never silently
// skipped). These accompany the local plan when no live push runs.
func guidedSteps(sn, slug, baseURL string, creds store.BillingCredentials, mapped int, applyErr, rtdnToken string) []*adminv1.GuidedStep {
	var steps []*adminv1.GuidedStep
	if applyErr != "" {
		steps = append(steps, &adminv1.GuidedStep{
			Title:  "Store push reported an error",
			Detail: applyErr,
		})
	}
	base := strings.TrimSuffix(baseURL, "/")
	if sn == store.SubscriptionStoreApple {
		if !credentialsPresent(sn, creds) {
			steps = append(steps, &adminv1.GuidedStep{
				Title:  "Configure Apple billing credentials",
				Detail: "Add the App Store Server API In-App-Purchase key and bundle id (Monetization → store credentials) before pushing.",
			})
		}
		steps = append(steps, &adminv1.GuidedStep{
			Title:  "Push the Apple catalog with the CLI",
			Detail: "The App Store Connect catalog API key is never stored by moth. Run `moth setup billing` with the ASC key to create/update the subscriptions, then submit the new subscriptions for review in App Store Connect (no API).",
			Values: []*adminv1.GuidedValue{{Label: "command", Value: "moth setup billing --project " + slug + " --asc-p8 <path> --asc-key-id <id> --asc-issuer-id <id> --apple-app-id <id>"}},
		})
		steps = append(steps, &adminv1.GuidedStep{
			Title:  "Register the App Store Server Notification URL",
			Detail: "In App Store Connect → App Information → App Store Server Notifications (V2), set the Production and Sandbox URL to moth's endpoint.",
			Url:    "https://appstoreconnect.apple.com",
			Values: []*adminv1.GuidedValue{{Label: "notification URL", Value: base + "/billing/apple/notifications/" + slug}},
		})
		return steps
	}
	if sn == store.SubscriptionStoreStripe {
		if !credentialsPresent(sn, creds) {
			steps = append(steps, &adminv1.GuidedStep{
				Title:  "Configure Stripe billing credentials",
				Detail: "Add the Stripe restricted secret key (Monetization → store credentials) before pushing; the same key drives catalog provisioning and runtime.",
			})
		}
		if len(creds.StripeWebhookSecretEnc) == 0 {
			steps = append(steps, stripeWebhookGuidedStep(slug, baseURL))
		}
		return steps
	}
	if !credentialsPresent(sn, creds) {
		steps = append(steps, &adminv1.GuidedStep{
			Title:  "Configure Google billing credentials",
			Detail: "Add the Play Developer service-account JSON and package name (Monetization → store credentials) before pushing.",
		})
	}
	steps = append(steps, &adminv1.GuidedStep{
		Title:  "Confirm Play Console availability & pricing matrix",
		Detail: "The Android Publisher API provisions the base plan + base price; activate the subscription and set country availability in Play Console.",
		Url:    "https://play.google.com/console",
	})
	rtdnEndpoint := base + "/billing/google/rtdn/" + slug
	if rtdnToken != "" {
		rtdnEndpoint += "?token=" + url.QueryEscape(rtdnToken)
	}
	steps = append(steps, &adminv1.GuidedStep{
		Title:  "Point Play RTDN at moth's endpoint",
		Detail: "In Play Console → Monetization setup → Real-time developer notifications, set the topic and confirm the push subscription targets moth (the endpoint carries the shared-secret token the webhook authenticates).",
		Values: []*adminv1.GuidedValue{
			{Label: "RTDN endpoint", Value: rtdnEndpoint},
			{Label: "topic", Value: creds.GooglePubsubTopic},
		},
	})
	return steps
}
