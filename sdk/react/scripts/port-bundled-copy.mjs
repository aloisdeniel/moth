#!/usr/bin/env node
// Ports the Flutter SDK's bundled fallback copy catalog
// (sdk/flutter/lib/src/i18n/bundled_copy.dart) to TypeScript, mechanically,
// so the two SDKs share one localization floor. Run from sdk/react:
//
//   node scripts/port-bundled-copy.mjs
//
// The committed artifact is src/core/i18n/bundledCopy.ts. Web-only keys the
// Flutter SDK has no use for live in the `webExtras` map below and are merged
// into every locale on regeneration.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const dartFile = join(here, '../../flutter/lib/src/i18n/bundled_copy.dart')
const outFile = join(here, '../src/core/i18n/bundledCopy.ts')

// Keys that exist only on the web (the Flutter floor has no equivalent).
const webExtras = {
  en: { 'paywall.unavailable_web': 'Not available for purchase on the web' },
  fr: { 'paywall.unavailable_web': "Indisponible à l'achat sur le web" },
  de: { 'paywall.unavailable_web': 'Im Web nicht erhältlich' },
  es: { 'paywall.unavailable_web': 'No disponible para comprar en la web' },
  pt: { 'paywall.unavailable_web': 'Indisponível para compra na web' },
  it: { 'paywall.unavailable_web': "Non disponibile per l'acquisto sul web" },
  ja: { 'paywall.unavailable_web': 'ウェブでは購入できません' },
}

const source = readFileSync(dartFile, 'utf8')

// Locale order from the Dart mothBundledLocales list.
const localesMatch = source.match(
  /mothBundledLocales = <String>\[([\s\S]*?)\]/,
)
if (!localesMatch) throw new Error('mothBundledLocales not found')
const locales = [...localesMatch[1].matchAll(/'([a-zA-Z-]+)'/g)].map(
  (m) => m[1],
)

// One Dart single-quoted string literal (handles \' and other escapes).
const literal = /'((?:[^'\\]|\\.)*)'/y

function unescapeDart(s) {
  return s
    .replace(/\\'/g, "'")
    .replace(/\\"/g, '"')
    .replace(/\\n/g, '\n')
    .replace(/\\\\/g, '\\')
}

// Parse each locale's map block: entries are 'key': 'value' (values may be
// several adjacent literals across lines — Dart concatenates them).
function parseLocale(code) {
  const start = source.indexOf(`'${code}': <String, String>{`)
  if (start < 0) throw new Error(`locale block not found: ${code}`)
  const open = source.indexOf('{', start)
  const close = source.indexOf('\n  },', open)
  const body = source.slice(open + 1, close)
  const entries = {}
  const entryRe = /'((?:[^'\\]|\\.)*)'\s*:/g
  let m
  while ((m = entryRe.exec(body)) !== null) {
    const key = unescapeDart(m[1])
    let i = entryRe.lastIndex
    let value = ''
    for (;;) {
      // Skip whitespace.
      while (i < body.length && /\s/.test(body[i])) i++
      literal.lastIndex = i
      const lit = literal.exec(body)
      if (!lit) break
      value += unescapeDart(lit[1])
      i = literal.lastIndex
    }
    entries[key] = value
    entryRe.lastIndex = i
  }
  return entries
}

const catalog = {}
for (const code of locales) {
  catalog[code] = { ...parseLocale(code), ...(webExtras[code] ?? {}) }
}

// English must be the complete key set; every other locale mirrors it.
const enKeys = Object.keys(catalog.en)
for (const code of locales) {
  const missing = enKeys.filter((k) => !(k in catalog[code]))
  if (missing.length > 0) {
    console.warn(`warning: ${code} missing keys: ${missing.join(', ')}`)
  }
}

const ts = `// GENERATED by scripts/port-bundled-copy.mjs from the Flutter SDK's
// lib/src/i18n/bundled_copy.dart — do not edit by hand; re-run the script
// when the Dart catalog changes. Web-only keys (paywall.unavailable_web)
// come from the script's webExtras map.
//
// The SDK's bundled fallback copy: the localization FLOOR for the SDK
// screens (sign_in.*, sign_up.*, password_reset.*, paywall.* and the shared
// error.* messages). It renders localized copy before GetProjectConfig
// arrives and offline. Server-delivered project copy overrides it when
// present; a locale neither side bundles falls back to English per key.

/** The BCP-47 language codes the SDK bundles fallback copy for, English first. */
export const mothBundledLocales: readonly string[] = ${JSON.stringify(locales)}

const bundled: Record<string, Record<string, string>> = ${JSON.stringify(catalog, null, 2)}

/**
 * The bundled fallback copy for \`locale\`'s language, as a message key ->
 * string map, with English filled in for any key the language lacks (so
 * every bundled-screen key always resolves non-empty). A language the SDK
 * does not bundle returns the full English map.
 */
export function bundledCopy(locale: string): Record<string, string> {
  const en = bundled['en']!
  const language = locale.split(/[-_]/)[0]?.toLowerCase() ?? 'en'
  const lang = bundled[language]
  if (lang === undefined) return { ...en }
  const merged: Record<string, string> = {}
  for (const key of Object.keys(en)) {
    const value = lang[key]
    merged[key] = value !== undefined && value !== '' ? value : en[key]!
  }
  return merged
}
`

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(outFile, ts)
console.log(
  `wrote ${outFile}: ${locales.length} locales, ${enKeys.length} keys`,
)
